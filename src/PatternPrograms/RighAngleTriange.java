package PatternPrograms;

public class RighAngleTriange {
    public static void main(String[] args) {
        int i,j,n;
        n=5;
        for (i=1;i<=5;i++){
            for(j=1;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
